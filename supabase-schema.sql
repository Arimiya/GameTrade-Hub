create or replace function public.release_order_to_seller(
  p_order_id bigint,
  p_buyer_id uuid,
  p_commission_rate numeric default 10
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order public.orders;
  v_wallet_id bigint;
  v_commission numeric(12, 2);
  v_seller_amount numeric(12, 2);
begin
  if auth.uid() <> p_buyer_id and not public.is_admin() then
    raise exception 'Not allowed';
  end if;

  select * into v_order
  from public.orders
  where id = p_order_id and buyer_id = p_buyer_id
  for update;

  if v_order.id is null then
    raise exception 'Order not found';
  end if;
  if v_order.status in ('DISPUTED', 'REFUNDED', 'COMPLETED', 'SELLER_PAID') then
    raise exception 'Order cannot be released from current status';
  end if;

  select id into v_wallet_id from public.wallets where user_id = p_buyer_id for update;
  v_commission = round(v_order.held_amount * (p_commission_rate / 100), 2);
  v_seller_amount = v_order.held_amount - v_commission;

  update public.wallets
  set held_balance = greatest(held_balance - v_order.held_amount, 0),
      updated_at = now()
  where id = v_wallet_id;

  insert into public.seller_balances (seller_id, available_balance)
  values (v_order.seller_id, v_seller_amount)
  on conflict (seller_id) do update
  set available_balance = public.seller_balances.available_balance + excluded.available_balance,
      updated_at = now();

  insert into public.platform_commissions (order_id, amount, rate)
  values (v_order.id, v_commission, p_commission_rate);

  insert into public.wallet_transactions (wallet_id, user_id, transaction_type, amount, status, reference)
  values
    (v_wallet_id, p_buyer_id, 'COMMISSION', v_commission, 'COMPLETED', 'ORDER-' || v_order.id),
    (v_wallet_id, p_buyer_id, 'SELLER_RELEASE', v_seller_amount, 'SELLER_PAID', 'ORDER-' || v_order.id);

  update public.orders
  set status = 'COMPLETED',
      commission_amount = v_commission,
      updated_at = now()
  where id = v_order.id
  returning * into v_order;

  return v_order;
end;
$$;

create or replace function public.start_buyer_inspection(
  p_order_id bigint,
  p_seller_id uuid,
  p_inspection_hours integer default 24
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order public.orders;
begin
  if p_inspection_hours not in (24, 48, 72) then
    raise exception 'Inspection period must be 24, 48, or 72 hours';
  end if;

  select * into v_order
  from public.orders
  where id = p_order_id
  for update;

  if v_order.id is null then
    raise exception 'Order not found';
  end if;
  if auth.uid() <> p_seller_id and not public.is_admin() then
    raise exception 'Not allowed';
  end if;
  if v_order.status = 'DISPUTED' then
    raise exception 'Inspection cannot start while disputed';
  end if;

  update public.orders
  set status = 'DELIVERED',
      inspection_period_hours = p_inspection_hours,
      inspection_started_at = now(),
      auto_release_at = now() + make_interval(hours => p_inspection_hours),
      updated_at = now()
  where id = p_order_id
  returning * into v_order;

  return v_order;
end;
$$;

create or replace function public.auto_release_order_if_due(
  p_order_id bigint
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order public.orders;
begin
  select * into v_order
  from public.orders
  where id = p_order_id
  for update;

  if v_order.id is null then
    raise exception 'Order not found';
  end if;
  if v_order.status = 'DISPUTED' then
    raise exception 'Auto-release blocked by dispute';
  end if;
  if v_order.auto_release_at is null or v_order.auto_release_at > now() then
    raise exception 'Inspection period has not expired';
  end if;

  return public.release_order_to_seller(v_order.id, v_order.buyer_id, 10);
end;
$$;

create or replace function public.refund_order_to_buyer(
  p_order_id bigint,
  p_admin_note text default 'Refund approved'
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order public.orders;
  v_wallet_id bigint;
begin
  if not public.is_admin() then
    raise exception 'Only admin can refund held orders';
  end if;

  select * into v_order from public.orders where id = p_order_id for update;
  select id into v_wallet_id from public.wallets where user_id = v_order.buyer_id for update;

  update public.wallets
  set balance = balance + v_order.held_amount,
      held_balance = greatest(held_balance - v_order.held_amount, 0),
      updated_at = now()
  where id = v_wallet_id;

  insert into public.wallet_transactions (wallet_id, user_id, transaction_type, amount, status, reference)
  values (v_wallet_id, v_order.buyer_id, 'REFUND', v_order.held_amount, 'REFUNDED', p_admin_note);

  update public.orders
  set status = 'REFUNDED',
      held_amount = 0,
      updated_at = now()
  where id = v_order.id
  returning * into v_order;

  return v_order;
end;
$$;

create or replace function public.request_seller_withdrawal(
  p_seller_id uuid,
  p_amount numeric,
  p_payment_method text
)
returns public.withdrawals
language plpgsql
security definer
set search_path = public
as $$
declare
  v_balance public.seller_balances;
  v_withdrawal public.withdrawals;
begin
  if auth.uid() <> p_seller_id and not public.is_admin() then
    raise exception 'Not allowed';
  end if;
  if p_amount <= 0 then
    raise exception 'Withdrawal amount must be positive';
  end if;

  select * into v_balance from public.seller_balances where seller_id = p_seller_id for update;
  if v_balance.id is null or v_balance.available_balance < p_amount then
    raise exception 'Insufficient seller balance';
  end if;

  update public.seller_balances
  set available_balance = available_balance - p_amount,
      pending_balance = pending_balance + p_amount,
      updated_at = now()
  where seller_id = p_seller_id;

  insert into public.withdrawals (seller_id, amount, payment_method, status)
  values (p_seller_id, p_amount, p_payment_method, 'PENDING')
  returning * into v_withdrawal;

  return v_withdrawal;
end;
$$;
