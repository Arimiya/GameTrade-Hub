create or replace function public.process_deposit_success(
  p_user_id uuid,
  p_amount numeric,
  p_provider text,
  p_reference text
)
returns public.wallet_transactions
language plpgsql
security definer
set search_path = public
as $$
declare
  v_wallet_id bigint;
  v_transaction public.wallet_transactions;
begin
  if auth.uid() <> p_user_id and not public.is_admin() then
    raise exception 'Not allowed';
  end if;
  if p_amount <= 0 then
    raise exception 'Deposit amount must be positive';
  end if;

  insert into public.wallets (user_id, balance)
  values (p_user_id, 0)
  on conflict (user_id) do nothing;

  select id into v_wallet_id from public.wallets where user_id = p_user_id for update;

  update public.wallets
  set balance = balance + p_amount,
      updated_at = now()
  where id = v_wallet_id;

  insert into public.wallet_transactions (
    wallet_id,
    user_id,
    transaction_type,
    payment_method,
    amount,
    status,
    reference
  )
  values (
    v_wallet_id,
    p_user_id,
    'DEPOSIT',
    p_provider,
    p_amount,
    'DEPOSIT_SUCCESSFUL',
    p_reference
  )
  returning * into v_transaction;

  return v_transaction;
end;
$$;

create or replace function public.buy_with_wallet(
  p_buyer_id uuid,
  p_listing_id bigint,
  p_amount numeric
)
returns public.orders
language plpgsql
security definer
set search_path = public
as $$
declare
  v_wallet public.wallets;
  v_order public.orders;
  v_seller_id uuid;
begin
  if auth.uid() <> p_buyer_id and not public.is_admin() then
    raise exception 'Not allowed';
  end if;
  if p_amount <= 0 then
    raise exception 'Purchase amount must be positive';
  end if;

  select * into v_wallet from public.wallets where user_id = p_buyer_id for update;
  if v_wallet.id is null or v_wallet.balance < p_amount then
    raise exception 'Insufficient wallet balance';
  end if;

  select seller_id into v_seller_id from public.listings where id = p_listing_id;

  update public.wallets
  set balance = balance - p_amount,
      held_balance = held_balance + p_amount,
      updated_at = now()
  where id = v_wallet.id;

  insert into public.orders (buyer_id, seller_id, listing_id, amount, held_amount, status)
  values (p_buyer_id, v_seller_id, p_listing_id, p_amount, p_amount, 'PAYMENT_HELD')
  returning * into v_order;

  insert into public.order_payments (order_id, buyer_id, amount, status, provider)
  values (v_order.id, p_buyer_id, p_amount, 'PAYMENT_HELD', 'WALLET');

  insert into public.wallet_transactions (wallet_id, user_id, transaction_type, amount, status, reference)
  values (v_wallet.id, p_buyer_id, 'PURCHASE_HOLD', p_amount, 'PAYMENT_HELD', 'ORDER-' || v_order.id);

  return v_order;
end;
$$;

